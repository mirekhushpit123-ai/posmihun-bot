# --- ФУНКЦІЇ ---
def load_groups():
    if not os.path.exists(GROUPS_FILE): return set()
    with open(GROUPS_FILE, "r") as f: 
        try: return set(json.load(f))
        except: return set()

def save_group(chat_id):
    groups = load_groups()
    if chat_id not in groups:
        groups.add(chat_id)
        with open(GROUPS_FILE, "w") as f: json.dump(list(groups), f)

def get_joke_index():
    if not os.path.exists(JOKES_FILE): return 0
    with open(JOKES_FILE, "r") as f: return json.load(f)

def save_joke_index(index):
    with open(JOKES_FILE, "w") as f: json.dump(index, f)

# --- БОТ ---
@dp.message(Command("start"))
async def cmd_start(message: types.Message):
    save_group(message.chat.id)
    await message.answer("Привіт! Я бот ПоСмІхУнЧиК. Я допомагаю створювати чудовий настрій у чаті, надсилаючи ранкові привітання, мудрі слова та побажання доброго вечора за розкладом! 😊🌾")

async def broadcast(text, is_photo=False):
    for chat_id in load_groups():
        try:
            if is_photo:
                await bot.send_photo(chat_id=chat_id, photo="https://source.unsplash.com/800x600/?nature,morning", caption=text)
            else:
                await bot.send_message(chat_id=chat_id, text=text)
        except Exception as e: logging.error(f"Помилка в {chat_id}: {e}")

async def job_morning():
    date_str = datetime.datetime.now().strftime("%d %B %Y року")
    text = f"{random.choice(MORNING_GREETINGS)}\n\n📅 Сьогодні: {date_str}\n\n🧠 Мудрість дня: {random.choice(WISDOMS)}"
    await broadcast(text, is_photo=True)

async def job_joke():
    idx = get_joke_index()
    text = f"😂 Анекдот дня:\n\n{JOKES[idx]}"
    await broadcast(text)
    save_joke_index((idx + 1) % len(JOKES))

async def job_night():
    await broadcast("🌙 На добраніч! Солодких снів, відпочивай та набирайся сил на завтра! 💤")

async def main():
    scheduler = AsyncIOScheduler(timezone="Europe/Kyiv")
    scheduler.add_job(job_morning, "cron", hour=7, minute=0)
    scheduler.add_job(job_joke, "cron", hour=12, minute=30)
    scheduler.add_job(job_night, "cron", hour=23, minute=0)
    scheduler.start()
    await dp.start_polling(bot)

if __name__ == "__main__":
    asyncio.run(main())
